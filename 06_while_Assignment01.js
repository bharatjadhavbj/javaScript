
console.log(`=================================================While loop=================================================`);

console.log(`Step 1=====>Prog for numbers from 5 to 15 incrementing by 1===============================================`);

var i=5;
while (i<=15) {
    console.log(i);
    i++;  
}

console.log(`Step 2=====>Prog for numbers from 50 to 40 decrementing by 1==============================================`);


var i=50;
while (i>=40) {
    console.log(i);
    i--;  
}

